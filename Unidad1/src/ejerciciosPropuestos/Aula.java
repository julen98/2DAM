package ejerciciosPropuestos;

public class Aula {
	
	String nombre;
	int piso;
	
	public Aula(String nombre, int piso) {
		super();
		this.nombre = nombre;
		this.piso = piso;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getPiso() {
		return piso;
	}

	public void setPiso(int piso) {
		this.piso = piso;
	}
	

	
}
